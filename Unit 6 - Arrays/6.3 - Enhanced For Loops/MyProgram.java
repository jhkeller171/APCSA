/*

Given an array, use an Enhanced for loop to access and print each value.

*/

public class MyProgram
{
    public static void main(String[] args)
    {
        int[] tests = {90, 72, 81, 85, 98};
        
        // use an Enhanced for loop to print out 
        // each value in this array on its own line
        
       for (int value: tests){
           System.out.println(value);
       } 
       
    }
}
